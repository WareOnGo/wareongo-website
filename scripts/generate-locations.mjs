import fs from 'node:fs/promises';
import path from 'node:path';
import { fetchAllWarehouses, summarize, locationTypeCombos } from './lib/locations.mjs';

// Pivot the combo list into { PEB: LocationSummary[]; RCC: LocationSummary[] }
// for use by the footer link sections.
function groupByType(combos) {
  const out = { PEB: [], RCC: [] };
  for (const c of combos) {
    out[c.warehouseType].push({
      canonical: c.location.canonical,
      slug: c.location.slug,
      count: c.count,
    });
  }
  for (const key of Object.keys(out)) {
    out[key].sort((a, b) => a.canonical.localeCompare(b.canonical));
  }
  return out;
}

async function main() {
  const warehouses = await fetchAllWarehouses();
  const cities = summarize(warehouses, 'city');
  const states = summarize(warehouses, 'state');
  const cityByType = groupByType(locationTypeCombos(warehouses, 'city'));
  const stateByType = groupByType(locationTypeCombos(warehouses, 'state'));

  const body = `// AUTO-GENERATED by scripts/generate-locations.mjs — do not edit by hand.
// Regenerated on every \`npm run build\`. Source: backend warehouses API.

export interface LocationSummary {
  canonical: string;
  slug: string;
  count: number;
}

export interface LocationsByType {
  PEB: LocationSummary[];
  RCC: LocationSummary[];
}

export const CITIES: LocationSummary[] = ${JSON.stringify(cities, null, 2)};

export const STATES: LocationSummary[] = ${JSON.stringify(states, null, 2)};

export const CITIES_BY_TYPE: LocationsByType = ${JSON.stringify(cityByType, null, 2)};

export const STATES_BY_TYPE: LocationsByType = ${JSON.stringify(stateByType, null, 2)};
`;

  const outDir = path.join('src', 'data');
  const outPath = path.join(outDir, 'locations.generated.ts');
  await fs.mkdir(outDir, { recursive: true });
  await fs.writeFile(outPath, body, 'utf8');
  console.log(
    `[locations] wrote ${outPath} — ${cities.length} cities, ${states.length} states, ${cityByType.PEB.length}+${cityByType.RCC.length} city×type, ${stateByType.PEB.length}+${stateByType.RCC.length} state×type (from ${warehouses.length} listings)`,
  );
}

main().catch((err) => {
  console.error('[locations] generation failed:', err);
  process.exit(1);
});
